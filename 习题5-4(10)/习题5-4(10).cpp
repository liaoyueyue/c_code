//中国古代数学家张丘建提出的“百鸡问题”：一只大公鸡值5个钱，一只母鸡值3个钱，三只小鸡值1个钱。
//现在有100钱，要买100只鸡，是否可以？若可以，给出一个解，要求三种鸡都有。请写出求解该问题。
#include"stdio.h"
int main()
{
	int x, y,z;
	for(x=1;x<=20;x++)
		for(y=1;y<=33;y++)
			for (z = 1; z <= 297; z++)
			{
				if (5 * x + 3 * y + z * 1 / 3.0 == 100&& x + y + z == 100)
					if(x!=0&&y!=0&&z!=0)
							printf("公鸡%d只，母鸡%d只，小鸡%d\n", x, y, z);
			}
	return 0;
}